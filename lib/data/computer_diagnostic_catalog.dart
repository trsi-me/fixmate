/// محتوى مشكلة واحدة لمسار الحاسب.
class ComputerDiagnosticProblem {
  final String title;
  final List<String> causes;
  final List<String> solutions;
  final List<String> warnings;

  const ComputerDiagnosticProblem({
    required this.title,
    required this.causes,
    required this.solutions,
    required this.warnings,
  });
}

/// قسم رئيسي (مثل البطارية، الشاشة، …).
